my_list = [1,2,3,4,5,6]
# list is ordered , changeble , duplicates , indexing
# 1. Access the elements of list through indexing
print(my_list[1]) 
print("\n\n\nLine change")

# 2. Acess the elements through for loop
for item in my_list:
    print(item)
    

# 3. Access the elements through for loop by using range function
print("through range function")
for item in range(len(my_list)):
    print(my_list[item])









